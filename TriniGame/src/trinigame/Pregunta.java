public class Pregunta {
    
    private String Pregunta;
    private String Res1;
    private String Res2;
    private String Res3;
    private String Res4;
    
    public Pregunta(String pregunta, String res1, String res2, String res4){
        this.Pregunta = Pregunta;
        this.Res1 = Res1;
        this.Res2 = res2;
        this. Res3 = Res3;
        this.Res4 = Res4;
        
    }

    public String getPregunta() {
        return Pregunta;
    }

    public String getRes1() {
        return Res1;
    }

    public String getRes2() {
        return Res2;
    }

    public String getRes3() {
        return Res3;
    }

    public String getRes4() {
        return Res4;
    }
    
   
    
    
    
}
